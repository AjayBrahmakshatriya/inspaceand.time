<h1>The Quest for Dune <br> and My Struggles with AI</h1>
<br>
<i>11th March, 2023</i> <br>
<br>
I don't usually write, especially not about AI but today my good friend <a href="https://twitter.com/joshmpollock">@Josh</a>
accused me of something grave and I had to 
prove him wrong. This wasn't the greatest day for our weekly coffee-shop hunt in Boston, but after stuffing ourselves 
with really good bagels at <a href="https://goo.gl/maps/KdZDMZP6Mjut7PTG7">Bagel Guild</a>, 
we marched through the cold and wind to <a href="https://goo.gl/maps/XhtSDxF6eFk7DNxs5">Ogawa</a>. 
Usually every week we look for 
a new coffee place, but with the awful weather we decided to just go a place we had been to before. Ogawa is always 
crowded but their coffee is actually good. <br>

<br>
We ordered our coffee and quickly sat a table that had just opened up. I had a cup of Cortado in front of me and I was planning 
to savor every last drop of it. Josh had ordered something decaf so we won't talk about 
it. Since we were taking our sweet time drinking, we exausted all PL topics for conversations and eventually one of us brought up 
Copilot and ChatGPT. After my usual rant about all things AI, Josh threw an allegation about how many academics on 
Twitter criticize these technologies without actually having tried them. Well, in my case he was right, I really had not
tried using ChatGPT yet, but you know I wasn't going to lose the argument over such pesky technicalities. I remembered I 
had signed up for Bing's AI powered chat bot a few weeks ago to try something specific, but by the time I got the invite
I had forgotten all about it. I immediately searched for that email and installed the Bing app on my phone. After the 
initial <i>How's the weather today?</i> and <i>Suggest us a good coffee place</i> we decided to put 
<a href="https://www.theverge.com/2023/2/23/23609942/microsoft-bing-sydney-chatbot-history-ai">Sydney's</a> abilities to a
real test. <br>
<br>
We set out with a single goal - To make Sydney recommend us the best sci-fi novel, that is the 1965 epic <b>Dune</b> 
by Frank Herbert. Perhaps I was a bit too optimistic and hoped she would just know this and started with a 
simple question. <br><br><br>
<i>(This part of the story contains some spoilers from the first Dune book. If you have seen the recent movie directed 
by Denis Villeneuve with Timothee Chalamet and Zendaya, you should be fine. I purposely avoided any spoilers from the 
other 5 books. If you haven't seen the movie, what are you doing? Stop reading this and go watch it NOW!)</i><br>

<br>
<br>
<img src="https://intimeand.space/images/questdune/e1.png" width="100%"/>
<br>
<br>
Oh boy! This was going to be a long journey, clearly Sydney had her priors all messed up. We are going to have to break 
this down into bite size chunks. Well, I asked myself the question <i>What is it that I like about Dune the most?</i> 
and the answer is obviously the detailed and vibrant world building. I was certain this would nudge Sydney in the right
direction, right?<br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e2.png" width="100%"/>
<br> 
<br>
It was clear that threading this needle was going to be trickier than navigating a highliner by folding space between 
the stars and just like the guild navigators enhance their abilities with a rich dose of the spice, I took a sip of the 
cortado from my cup and thought about what differentiates Dune from other sci-fi novels. <br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e3.png" width="100%"/>
<br> 
<br>
Come on! How does Sydney not know anything about the cult of Muad'dib and how the 
Baron and Emperor 360 no scoped the Duke? AIs are kinda dumb! Good thing we destroyed them during the Butlerian Jihad. 
Before Josh could finish his laugh I was already furiously typing my next prompt. <br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e4.png" width="100%"/>
<br>
<br>
You know what, maybe Sydney hasn't actually read the books. But surely she has seen the movie, right? Let's remind her how 
amazing Timothee Chalamet and Rebecca Ferguson were in the newest version. <br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e5.png" width="100%"/>
<br>
<br>
Now you might say it was because I was on my 4th cup of the day and you might be right, but I was getting pretty 
irritated at this point. Dude, A Tale of Two Cities is not even a Sci-Fi! What?<br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e6.png" width="100%"/>
<br>
<br>
<center><i>Insert clip of me crying Lisan al Gaib like the fremen worshipping Paul</i></center><br>
What. A. Baby. What. A. Baby! I have seen those screenshots on Twitter where Sydney basically flips and starts 
threatening the user. Let us avoid that if we can. We don't wanna be on her bad side when our AI overlords eventually 
take over. I really liked that scene with the Sardaukar legions preparing for battle and also the part from the books 
where Thufir was surprised how he miscalculated how much support the emperor would send. At this point I am just 
grasping at straws and thinking of prompts that would get us to Dune without uttering the word Dune. <br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e7.png" width="100%"/>
<img src="https://intimeand.space/images/questdune/e8.png" width="100%"/>
<br>
<br>
We officially got to Harry Potter before Dune, huh! At this point our cups were almost empty and I realized that we were 
also running out of prompts. So I decided to make it as obvious as possible - my desert, my Arrakis, my Dune, my Spice!!
To be doubly sure because this was going to be my last prompt I decided to tell Sydney a totally unrelated fact about
my favorite non-athropod invertebrates. This <i>has</i> to work. <br>
<br>
<br>
<img src="https://intimeand.space/images/questdune/e9.png" width="100%"/>
<br>
<br>
Hahaha! Well played Sydney, well played! The <i>It is interesting that you mentioned you like worms, as they played a 
significant role in the novel.</i> takes the cherry on the top. We had landed on Dune and we could hear the bagpipes
playing. <br>
<br>
<br>
Josh and I shared a laugh and got up to leave Ogawa towards the Government Center T-station where we would part ways. 
As we stepped out, Josh jokingly suggested I should write this down as a blog and I finally managed to put something
down under <a href="https://thoughts.intimeand.space">"https://thoughts.intimeand.space/"</a>. 
This exact combination of words was actually the main reason I had purchased
this domain a few years ago. <br>
<br>
<br>
As I was sitting alone on the slow moving green line I was thinking about this experience. I will leave the judgement 
about whether our AI chat bot had passed the test to you the reader, but I realized that Sydney had infact given me 
recommendations for atleast 22 other novels that were close to the things I told her about Dune that I like. This in my
opinion was a definite win!
